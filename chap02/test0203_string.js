/**
 * - String 타입
 */

// 문자열 리터럴
var s = "hello world";
console.log(typeof s);

// 객체
var o = new String("hello world");
console.log(typeof o);

// 값을 문자열로 변환하는데 사용.
var v1 = new Date();
console.log(typeof String(v1));
console.log(String(v1));
var v2 = 1234;
console.log(typeof String(v2));
console.log(String(v2));
